    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        // TODO add your handling code here:
        Mascota mascota =new Mascota();
        mascota.setNombre(jTextFieldNombre.getText());
        mascota.setEspecie(jTextFieldEspecie.getText());
        mascota.setColor(jTextFieldColor.getText());
        mascota.setSexo(jTextFieldSexo.getText());
        mascota.setEdad(Integer.parseInt(jTextFieldEdad.getText()));
        
        MascotaDB mascotaDB=new MascotaDB();
        try {
            mascotaDB.insertarMascota(mascota);
            JOptionPane.showMessageDialog(this, "Mascota registrada de manera exitosa", "Confirmación",JOptionPane.INFORMATION_MESSAGE);
            
            jTextFieldNombre.setText("");
            jTextFieldColor.setText("");
            jTextFieldEdad.setText("");
            jTextFieldEspecie.setText("");
            jTextFieldSexo.setText("");
            cargarMascotas();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Ocurrio un error al insertar la mascota","Error Base de datos",JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(GestionMascotas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

